import matplotlib.pyplot as plt
import librosa
# --- 2. DEFINICIÓN DEL GATILLO (TRIGGER) RMS ---

def pipeline_safevoice_completo(path, sensibilidad=0.3):
    # Cargar audio a 16kHz (Frecuencia que requiere YAMNet)
    y, sr = librosa.load(path, sr=16000, mono=True)

    # Calcular energía RMS (Root Mean Square)
    # El hop_length 512 da una buena resolución temporal
    rms = librosa.feature.rms(y=y, hop_length=512)[0]

    # Normalizar de 0 a 1 para que la sensibilidad sea estándar
    rms_norm = (rms - np.min(rms)) / (np.max(rms) - np.min(rms) + 1e-6)

    # Buscar el momento exacto donde se supera el umbral
    trigger_index = -1
    for i, nivel in enumerate(rms_norm):
        if nivel > sensibilidad:
            trigger_index = i
            break

    if trigger_index != -1:
        tiempo_inicio = librosa.frames_to_time(trigger_index, sr=sr, hop_length=512)
        print(f"\n [ANOMALÍA DETECTADA] en el segundo: {tiempo_inicio:.2f}")

        # --- CAPTURA DE 10 SEGUNDOS PARA LA IA ---
        inicio_samples = int(tiempo_inicio * sr)
        fin_samples = inicio_samples + (10 * sr)
        segmento_ia = y[inicio_samples:fin_samples]

        # --- VISUALIZACIÓN ---
        plt.figure(figsize=(10, 3))
        plt.plot(rms_norm, label='Energía detectada', color='blue')
        plt.axhline(y=sensibilidad, color='red', linestyle='--', label='Umbral de Alerta')
        plt.axvline(x=trigger_index, color='orange', label='Punto de Disparo')
        plt.title("Visualización del Gatillo RMS (Nivel de Energía)")
        plt.xlabel("Tramas de tiempo")
        plt.ylabel("Energía Normalizada")
        plt.legend()
        plt.show()

        # --- REPRODUCTOR DEL RECORTE ---
        print(f"▶️ ESCUCHANDO EL RECORTE DE 10s (Lo que recibirá YAMNet):")
        display(Audio(segmento_ia, rate=sr))

        return segmento_ia
    else:
        print("El audio ambiental no superó el umbral de sensibilidad.")
        return None

