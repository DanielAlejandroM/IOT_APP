import io

def preprocesar_audio(audio_raw):
    # Normalización de pico: hace que el valor más alto sea 1.0 o -1.0
    if np.max(np.abs(audio_raw)) > 0:
        audio_norm = audio_raw / np.max(np.abs(audio_raw))
    else:
        audio_norm = audio_raw
    return audio_norm
def calcular_cercania(v1, v2):
    return np.dot(v1, v2) / (np.linalg.norm(v1) * np.linalg.norm(v2))

